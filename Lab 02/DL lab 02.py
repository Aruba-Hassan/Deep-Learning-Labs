import numpy as np
import matplotlib.pyplot as plt
def leaky_relu(x, alpha=0.1):
  return np.where(x >= 0, x , alpha * x)
inputs = np.array([-3.0,-1.0,0.0,2.0])
output = leaky_relu(inputs)
print("Output After Leaky_Relu Function Applied:" , output)
x = np.linspace(-5,5,100)
y1 = leaky_relu(x, 0.1)
y2 = leaky_relu(x, 0.3)
plt.plot(x,y1,label= "alpha=0.1")
plt.plot(x,y2,label= "alpha=0.3")
plt.legend()
plt.title("Leaky_Relu Function Plotting")
plt.grid()
plt.show()

import numpy as np
import matplotlib.pyplot as plt
def softmax(x):
  e_x = np.exp(x)
  return e_x/np.sum(e_x)
inputs = np.array([1.0,2.0,3.0])
output = softmax(inputs)
print("Output After Softmax Function Applied:", output)
print("Sum:" , np.sum(output))

