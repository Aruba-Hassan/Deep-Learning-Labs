import numpy as np
import matplotlib.pyplot as plt
def step_function(x):
  return np.where(x >= 0,1,0)
inputs = np.array([-3.0,-1.0,0.0,2.0,5.0])
output = step_function(inputs)
print("Output After Step Function Applied:",output)
x = np.linspace(-5,5,100)
y = step_function(x)
plt.plot(x, y)
plt.title("Step Function Plotting")
plt.grid()
plt.show()

import numpy as np
import matplotlib.pyplot as plt
def relu(x):
  return np.maximum(0, x)
inputs = np.array([-4.0,-2.0,0.0,3.0,5.0])
output = relu(inputs)
print("Output After Relu Function Applied:", output)
x = np.linspace(-5,5,100)
y = relu(x)
plt.plot(x,y)
plt.title("Relu Function Plotting")
plt.grid()
plt.show()

import numpy as np
import matplotlib.pyplot as plt
def sigmoid(x):
  return 1/(1+ np.exp(-x))
inputs = np.array([-2.0,0.0,1.0,3.0])
output = sigmoid(inputs)
print("Output After Sigmoid Function Applied:", output)
x = np.linspace(-5,5,100)
y = sigmoid(x)
plt.plot(x,y)
plt.title("Sigmoid function Plotting")
plt.grid()
plt.show()

import numpy as np
import matplotlib.pyplot as plt
def tanh(x):
  return np.tanh(x)
