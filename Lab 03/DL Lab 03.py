class OvRPerceptron:
    def __init__(self, lr=0.1, epochs=20):
        self.lr = lr
        self.epochs = epochs
        self.weights = {}
    def train_binary(self, X, y_binary):
        X = np.insert(X, 0, 1, axis=1)
        w = np.zeros(X.shape[1])
        for _ in range(self.epochs):
            j = np.random.randint(len(X))
            pred = step(np.dot(X[j], w))
            w += self.lr * (y_binary[j] - pred) * X[j]
        return w
    def fit(self, X, y):
        self.classes = np.unique(y)
        for c in self.classes:
            y_binary = (y == c).astype(int)
            self.weights[c] = self.train_binary(X, y_binary)
    def predict(self, X):
        X = np.insert(X, 0, 1, axis=1)
        results = []
        for x in X:
            scores = {}
            for c, w in self.weights.items():
                scores[c] = np.dot(x, w)
            results.append(max(scores, key=scores.get))
        return np.array(results)
