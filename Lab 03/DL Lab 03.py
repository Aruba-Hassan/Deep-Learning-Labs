import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from google.colab import files
uploaded = files.upload()
file_name = list(uploaded.keys())[0]
df = pd.read_csv(file_name)
df.columns = df.columns.str.strip()
df = df.dropna()
y = df["Target"].values
X = df[["Rotational speed [rpm]", "Torque [Nm]"]].values
X = X.astype(float)
X = (X - X.mean(axis=0)) / X.std(axis=0)
def step(z):
    return 1 if z > 0 else 0
class Perceptron:
    def __init__(self, lr=0.01, epochs=20):
        self.lr = lr
        self.epochs = epochs
    def fit(self, X, y):
        X = np.insert(X, 0, 1, axis=1)
        self.w = np.zeros(X.shape[1])
        for _ in range(self.epochs):
            idx = np.random.permutation(len(X))
            Xs, ys = X[idx], y[idx]
            for xi, target in zip(Xs, ys):
                pred = step(np.dot(xi, self.w))
                self.w += self.lr * (target - pred) * xi
        return self
models = {}
for lr in [0.01, 0.1]:
    model = Perceptron(lr=lr, epochs=20)
    model.fit(X, y)
    models[lr] = model
for lr in [0.01, 0.1]:
    model = models[lr]
    w = model.w
    plt.figure(figsize=(6,5))
    plt.scatter(X[:,0], X[:,1], c=y, cmap="bwr", alpha=0.6)
    x_min, x_max = X[:,0].min(), X[:,0].max()
    xs = np.linspace(x_min, x_max, 100)
    ys = -(w[0] + w[1]*xs) / w[2]
    plt.plot(xs, ys, 'k--', label="Decision Boundary")
    plt.xlabel("Rotational speed [rpm]")
    plt.ylabel("Torque [Nm]")
    plt.title(f"Perceptron (Learning Rate = {lr})")
    plt.legend()
    plt.grid()
    plt.show()